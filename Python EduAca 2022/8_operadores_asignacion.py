# Los operadores son simboles que indican como se manipulan los datos

# Operadores de asignación  - Asignan un valor a una variable. Se pueden combinar con valores aritméticos.
"""
+=  - Suma
-=  - Resta
*=  - Multiplica
/=  - Divide
%=  - Resto
"""
numero1 = 5

# Con la asignación se le asigna de manera rapida un nuevo valor a la variable numero1.

# Suma
numero1 += 5
print(numero1)

# Resta
numero1 -= 5
print(numero1)

# Multiplicación
numero1 *= 5
print(numero1)

# División
numero1 /= 5
print(numero1)

# Modulo
numero1 %= 5
print(numero1)
